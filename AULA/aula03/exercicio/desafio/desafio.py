categoria = int(input('Me de a categoria entre (1 a 5): '))

if categoria == 1 :
    print("Preço: 10.00")
    
elif categoria == 2 :
    print("Preço: 18.00")
        
elif categoria == 3 :
    print("Preço: 23.00")
        
elif categoria == 4 :
    print("Preço: 26.00")
    
elif categoria == 5 :
    print("Preço: 31.00")

# caso não seja encotrado nem uma das 5 opções vai mostras o else
else:
    print('Categoria não encotrada\n\n')